const EMPTY_RESPONSE = { data: {} };

function extractSkuFromQuery(url, body) {
  const skuMatch = body?.match(/"sku"\s*:\s*"([^"]+)"/)
    || url.match(/sku["%3A:]+([a-zA-Z]+-\d+)/i)
    || url.match(/["%22]([mM][vV]-\d+)["%22]/);
  return skuMatch?.[1] || null;
}

const originalFetch = window.fetch.bind(window);

window.fetch = async (resource, init) => {
  let url = '';
  if (typeof resource === 'string') {
    url = resource;
  } else if (resource instanceof URL) {
    url = resource.href;
  } else if (resource instanceof Request) {
    url = resource.url;
  } else if (resource?.href || resource?.url) {
    url = resource.href || resource.url;
  }

  if (url.includes('localhost:9999')) {
    let body = '';
    if (init?.body && typeof init.body === 'string') {
      body = init.body;
    } else if (resource instanceof Request) {
      try { body = await resource.clone().text(); } catch { /* empty */ }
    }

    const isRecsQuery = url.includes('ecommend') || body.includes('ecommend');
    if (isRecsQuery) {
      return new Response(JSON.stringify(MOCK_RECS), {
        status: 200,
        headers: { 'Content-Type': 'application/json' },
      });
    }

    const isSearchQuery = url.includes('productSearch') || body.includes('productSearch');
    if (isSearchQuery) {
      return new Response(JSON.stringify(buildSearchResponse()), {
        status: 200,
        headers: { 'Content-Type': 'application/json' },
      });
    }

    const isAttributeQuery = url.includes('attributeMetadata') || body.includes('attributeMetadata');
    if (isAttributeQuery) {
      return new Response(JSON.stringify({
        data: {
          attributeMetadata: {
            sortable: [
              {
                label: 'Position',
                attribute: 'position',
                numeric: true,
                bidirectional: true,
              },
              {
                label: 'Product Name',
                attribute: 'name',
                numeric: false,
                bidirectional: true,
              },
              {
                label: 'Price',
                attribute: 'price',
                numeric: true,
                bidirectional: true,
              },
            ],
            filterableInSearch: [],
          },
        },
      }), {
        status: 200,
        headers: { 'Content-Type': 'application/json' },
      });
    }

    const sku = extractSkuFromQuery(url, body)
      || window.location.pathname.match(/\/products\/[\w|-]+\/([\w|-]+)$/)?.[1];
    if (sku) {
      return new Response(JSON.stringify(buildPdpResponse(sku)), {
        status: 200,
        headers: { 'Content-Type': 'application/json' },
      });
    }

    return new Response(JSON.stringify(EMPTY_RESPONSE), {
      status: 200,
      headers: { 'Content-Type': 'application/json' },
    });
  }

  return originalFetch(resource, init);
};

